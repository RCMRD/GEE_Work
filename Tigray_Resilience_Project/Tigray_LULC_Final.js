// Function to cloud mask from the pixel_qa band of Landsat 8 SR data.
function maskLSR(image) {
  // Bits 3 and 5 are cloud shadow and cloud, respectively.
  var cloudShadowBitMask = 1 << 3;
  var cloudsBitMask = 1 << 5;

  // Get the pixel QA band.
  var qa = image.select('pixel_qa');

  // Both flags should be set to zero, indicating clear conditions.
  var mask = qa.bitwiseAnd(cloudShadowBitMask).eq(0)
      .and(qa.bitwiseAnd(cloudsBitMask).eq(0));

  // Return the masked image, scaled to TOA reflectance, without the QA bands.
  return image.updateMask(mask).divide(10000)
      .select("B[0-9]*")
      .copyProperties(image, ["system:time_start"]);
}


function classify_ftn(c_opt, training, bands){
  
      
      switch (c_opt) {
        case CART:
            
                // Get a CART classifier and train it.
                classifier = ee.Classifier.cart().train({
                  features: training, 
                  classProperty: 'CLASS', 
                  inputProperties: bands
                });
                    
            break;
            
        case SVM:
            
            var svm_opt = {
                        kernelType: 'RBF',
                        gamma: 0.5,
                        cost: 10
                      };
            
                // Get a SVM classifier and train it.
                classifier = ee.Classifier.svm(svm_opt).train({
                  features: training, 
                  classProperty: 'CLASS', 
                  inputProperties: bands
                });
                
            break;
            
            
        case RANDOM_FOREST:
            
            classifier = ee.Classifier.randomForest({
                numberOfTrees: 200,//was less
                seed: 1
              }).train({
                  features: training, 
                  classProperty: 'CLASS', 
                  inputProperties: bands
                });
            
            break;
            
        case "MAXENT":
            
            classifier = ee.Classifier.gmoMaxEnt().train({
                  features: training, 
                  classProperty: 'CLASS', 
                  inputProperties: bands
                });
                
                break;
                
        default:
        
                break;
            
        }
  
  return classifier;
  
}


